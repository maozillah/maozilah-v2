		At this stage I define my <strong>context & design problem</strong>; potentially creating design artifacts to help me empathize and understand the target users.</strong> </p>

		<h2>3. Ideate</h2>
		<p>Research and thoughts become <strong>mind maps & brainstorms.</strong> Ideas are scribbled down and sketches are small and drawn in rapid successions. </p>
		
		<p>Concepts are <em>reviewed, evaluated, & tossed</em>. A few rough gems of concepts are selected as the seed for further ideation until a final concept is chosen.</p>

		<h2>4. Execute</h2>
		<p>Work smart instead of hard. This means having an awareness of and choosing the most efficient and effective tools for the job. Be it photoshop, code, or pen and paper; <strong>the idea dictates the strategy and tools. </strong></p>

		<h2>5. Iterate</h2>
		<p>Design is <em>never finished</em>; it merely becomes good enough to solicit feedback. Sharing, critiques, and user testing provide feedback that is used to fuel ongoing design iterations. </p>

