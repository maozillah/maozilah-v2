<h1>Background</h1>
<p>The project prompt was to design a physical installation to promote an organization. I chose the Inclusive Design Research Centre because I\'m fascinated by inclusive design.</p>

<h2>Inclusive Design Research Centre (IDRC)</h2>
<p>The <a href="http://idrc.ocadu.ca/" target="_blank">IDRC</a> is a research and development centre at OCAD University that consists of an international community of open source developers, designers who work together to ensure that emerging information technology and practices are designed inclusively.</p>
<p>Their goal is to promote digital inclusion by creating tools, strategies and resources that support the full range of human diversity with respect to ability, language, culture, gender, age and other forms of human difference.</p>

<h2>Installation Goal</h2>
<p>Design an environment that creates <b>empathy and understanding</b> for the importance of <b>digital accessibility</b>, as well as encourages proactive implementation of web accessibility standards among conference attendees of a digital technology conference.</p>

