<h2>Wireframes</h2>
<p>Some of the struggles we faced designing that app was making sure to respect people\'s concept of time. People see time as continuous. As a result, it was important that we didn\'t only show available days, but the unavailable days as well. Otherwise people may confuse days placed next to each other as continuous.</p>
