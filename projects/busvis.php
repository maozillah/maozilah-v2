<h2>Project Critiques</h2>
<p>Reflecting on this project now in 2015, there are many glaring issues that I see.</p>
<ul>
	<li>Far <strong>too many colours</strong> were chosen for visualizing the number of people. This makes it difficult to decipher the information communicated. As well, this granular level of data is unnecessary and does not add value to the visualization.</li>
